package homework_week_6;
//program to convert given uppercase string into lowercase string
public class UpperCaseToLowerCase {

    public static void main(String[] args)
    {
        String str1;
        str1 = "THE QUICK BROWN FOX JUMPS OVER THE LAZY DOG";//value of string is predefined
        System.out.println(str1.toLowerCase());// converting uppercase to lowercase by using toLowerCase() method
    }
}
